    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // Verificar si hay corredores registrados
    if (runnerCount != 5) {
        JOptionPane.showMessageDialog(null, "No hay suficientes corredores registrados.");
        return;
    }

    // Limpiar el JTextArea de resultados antes de iniciar la carrera
    jTextArea2.setText("");

    // Crear y ejecutar hilos para cada corredor registrado
    for (Runner corredor : corredores) {
        if (corredor != null) {
            Thread thread = new Thread(new ThreadRunner(corredor, jTextArea2));
            thread.start();
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

