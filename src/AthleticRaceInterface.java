    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       //
       String textoIngresado = jTextField1.getText();
       
       if (textoIngresado.isEmpty()){
           JOptionPane.showMessageDialog(null, "El nombre del participante no puede estar vacío.");
       } else if (runnerCount == 5){
            JOptionPane.showMessageDialog(null, "Ya se han registrado 5 corredores.");
       } else {
            Runner corredor = new Runner(textoIngresado);
            corredores[runnerCount] = corredor;
            runnerCount += 1;
            jTextField1.setText("");
            
            //Crear lista
           StringBuilder listaCorredores = new StringBuilder();
           int contador = 1;
           for (Runner newCorredor : corredores) {
               if (newCorredor != null) {
                   listaCorredores.append(contador).append(". ").append(newCorredor.getName()).append("\n");
                   contador++;
               }
           }
           jTextArea1.setText(listaCorredores.toString());
           
           
       }
       
       System.out.println(java.util.Arrays.toString(corredores));
    }//GEN-LAST:event_jButton1ActionPerformed

